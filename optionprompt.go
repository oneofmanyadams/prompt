package prompt

import (
	"io"
	"os"
)

type OptionPrompt struct {
	Question string
	Options  []string
	Answer   string
	Ask      bool

	Input  io.Reader
	Output io.Reader

	UserInput string
}

////////////////////////////////////////////////////////////////////////////////
// Constructor
////////////////////////////////////////////////////////////////////////////////

// New should be the only method for creating an OptionPrompt instance.
func New(question string) OptionPrompt {
	return OptionPrompt{
		Question: question,
		Ask:      true,
		Input:    os.Stdin,
		Output:   os.Stdout}
}

